
# Input Example 38


age = int(input("Enter Your Age: "))

if age > 10 and age < 100:
    print(f"Your Age In Months Is {age * 12} Months\n" + f"Your Age In Weeks Is {age * 12 * 4} Weeks")
else:
    print("Invalid Input")


